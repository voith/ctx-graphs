def ctx_holders():
    _file = open("ctx_addresses.csv", "a+")
    crawled_addresses = get_crawled_addresses()
    try:
        with Session(engine) as session:
            addresses = session.execute(ctx_query).all()
            for addr in addresses:
                if addr.val in crawled_addresses:
                    continue
                address = w3.toChecksumAddress(addr.val)
                print(f"scanning {address}")
                ctx_balance = ctx_contract.functions.balanceOf(
                    address).call() / 10.0 ** 18
                sushi_balance = sushiswap_contract.functions.balanceOf(
                    address).call()
                delegator_balance = 0
                for contract in delegator_contracts:
                    delegator_balance = contract.functions.stakerBalance(
                        address).call()
                    if delegator_balance > 0:
                        break
                _file.write(
                    ",".join([
                        address,
                        str(ctx_balance),
                        str(sushi_balance),
                        str(delegator_balance)
                    ])
                )
                _file.write("\n")
    except Exception as e:
        _file.close()
        raise e

    _file.close()


